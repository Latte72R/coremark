    .section .text.init, "ax", @progbits
    .globl _start
_start:
    .option push
    .option norelax
    la gp, __global_pointer$
    .option pop
    la sp, __stack_top

    la a0, __bss_start
    la a1, __bss_end
1:
    bgeu a0, a1, 2f
    sd zero, 0(a0)
    addi a0, a0, 8
    j 1b

2:
    call main
    tail exit
